#include <stdlib.h>
#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

char	*ft_strdup(char *src)
{	
	int		i;
	int		size;
	char	*dup;

	size = ft_strlen(src);
	dup = malloc(sizeof(char) * size + 1);
	if (dup == NULL)
		return ((char *) NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
